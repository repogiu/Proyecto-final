# Ejercicio ¿Cómo estuvo tu día (1 al 10)?
calificacion = int(input("¿Cómo estuvo tu día (1 al 10)?"))
if calificacion < 10 and calificacion > 1 :
    print("Tu dia estuvo de: ",calificacion)
else:
    print("Fuera del rango solicitado")

